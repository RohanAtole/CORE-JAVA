package Variables_DataTypes;

public class Assignmentoperator{
	
	public static void main(String args[]) {
		
		int num1=7;
		int num2=5;
		
		System.out.println(num1);
		
		//these three is same
		System.out.println("these three is same");
		
		num1--;
		System.out.println(num1);
		
		num1-=1;
		System.out.println(num1);
		
		num1=num1-1;
		System.out.println(num1);
		
		//these three is same
		System.out.println("these three is same");
		
		num1+=1;
		System.out.println(num1);
		
		num1++;
		System.out.println(num1);
		
		num1=num1+1;
		System.out.println(num1);
		
	}
}
