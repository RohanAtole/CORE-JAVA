package Variables_DataTypes;

public class datatypes{

	public static void main(String args[]) {
	
	//print values using datatypes
		
	//integer
	int a=10;
	System.out.println("number is"+a);
	
	//string
	String c="rohan";
	System.out.println("string is"+c);
	
	//double
	double d=1.5;
	System.out.println("double number"+d);
	
	//long
	long e=12000000;
	System.out.println("long number is"+e);
	
	//for character
	char f='R';
	System.out.println("character is "+f);
	
	//for boolean
	boolean g=true;
	System.out.println("boolean is "+g);
	
}
}
